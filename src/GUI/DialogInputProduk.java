    /**
     * Ambil data kategori dari database dan isi ke combobox.
     * Format yang ditampilkan: "Nama Kategori - Brand"
     * Contoh: "Elektronik - Samsung"
     */
    private void loadKategoriData() {
        Dao.KategoriDAO katDao = new Dao.KategoriDAO();
        
        // Ambil semua data kategori dari database
        listKategori = katDao.getAllKategori();
    
        // Hapus isi default combobox (yang dibuat NetBeans)
        cmbkategoriproduk.removeAllItems(); 
    
        // Masukkan data kategori satu per satu ke combobox
        for (Kategori kat : listKategori) {
            cmbkategoriproduk.addItem(kat.getKategori() + " - " + kat.getBrand());
        }
    }
