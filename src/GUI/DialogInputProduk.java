    /**
     * Tombol Save: simpan produk baru ke database.
     * Validasi yang dilakukan:
     * 1. Cek field kosong
     * 2. Cek tipe data angka (NumberFormatException)
     * 3. Cek nilai negatif
     */
    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // Ambil semua input dari form
        String nama = txtnama.getText().trim();
        int kategoriIndex = cmbkategoriproduk.getSelectedIndex();
        String hargaStr = txtharga.getText().trim();
        String stokStr = txtstock.getText().trim();

        // VALIDASI 1: Cek apakah ada data yang kosong
        if (nama.isEmpty() || kategoriIndex == -1 || hargaStr.isEmpty() || stokStr.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Semua data harus diisi!");
            return;
        }

        try {
            // VALIDASI 2: Konversi harga dan stok ke angka
            int harga = Integer.parseInt(hargaStr);
            int stok = Integer.parseInt(stokStr);

            // VALIDASI 3: Cek apakah nilai negatif
            if (harga < 0 || stok < 0) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "Harga dan Stok tidak boleh negatif!");
                return;
            }

            // Ambil kategori yang dipilih dari combobox
            Kategori katTerpilih = listKategori.get(kategoriIndex);

            // Bungkus data ke objek Produk
            Produk p = new Produk();
            p.setNama(nama);
            p.setKodeProduk(katTerpilih.getKode());
            p.setHarga(harga);
            p.setStock(stok);

            // Simpan ke database
            if (produkDao.insert(p)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Produk baru berhasil ditambahkan!");
                parentForm.loadDataToTable(); // Refresh tabel di form utama
                this.dispose(); // Tutup dialog
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan produk ke database.");
            }

        } catch (NumberFormatException ex) {
            // Kalau harga atau stok bukan angka
            javax.swing.JOptionPane.showMessageDialog(this, "Harga dan Stok harus berupa angka!");
        }
    }//GEN-LAST:event_btnsaveActionPerformed

