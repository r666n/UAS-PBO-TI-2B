    /**
     * Tombol Delete: hapus produk yang dipilih.
     * - Cek apakah ada baris yang dipilih
     * - Konfirmasi sebelum hapus
     */
    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // Cek apakah ada baris yang dipilih
        int selectedRow = tablelistproduk.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih produk yang akan dihapus.");
            return;
        }

        // Ambil kode produk dari baris yang dipilih
        int kodeProduk = (int) tablelistproduk.getValueAt(selectedRow, 0);
        
        // Konfirmasi penghapusan
        int confirm = javax.swing.JOptionPane.showConfirmDialog(
            this, 
            "Yakin ingin menghapus produk ini?", 
            "Konfirmasi Hapus", 
            javax.swing.JOptionPane.YES_NO_OPTION
        );
        
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            if (produkDao.delete(kodeProduk)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Produk berhasil dihapus.");
                loadDataToTable(); // Refresh tabel
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus produk.");
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed
    
