    /**
     * Load data produk dari database ke dalam tabel.
     * Data yang diambil sudah lengkap dengan kategori dan brand (hasil JOIN).
     * Dipanggil saat form dibuka, setelah insert/update/delete.
     */
    void loadDataToTable() {
        // Ambil model tabelnya
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tablelistproduk.getModel();
    
        // Kosongkan isi tabel dulu
        model.setRowCount(0);
    
        // Ambil semua data produk dari database (lengkap dengan JOIN kategori)
        this.listProduk = produkDao.getAllProduk();
        
        // Masukkan satu per satu ke tabel
        for (ClassModel.Produk p : listProduk) {
            Object[] row = {
                p.getKode(),
                p.getNama(),
                p.getHarga(),
                p.getStock(),
                p.getNamaKategori(), // Hasil JOIN dari tabel kategori
                p.getBrand()         // Hasil JOIN dari tabel kategori
            };
            model.addRow(row);
        }
    }
