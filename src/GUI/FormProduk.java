      /**
     * Tombol Update: buka dialog edit produk yang dipilih.
     * - Cek apakah ada baris yang dipilih
     * - Ambil data produk dari database
     * - Buka dialog update dengan data yang ada
     */
    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        int selectedRow = tablelistproduk.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih produk yang akan diupdate.");
            return;
        }

        int kodeProduk = (int) tablelistproduk.getValueAt(selectedRow, 0);
        
        Produk p = produkDao.getProdukById(kodeProduk);
        if (p == null) {
            JOptionPane.showMessageDialog(this, "Data produk tidak ditemukan.");
            return;
        }

        showUpdateDialog(p);
    }

