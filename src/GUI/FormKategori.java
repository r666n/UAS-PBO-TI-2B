    /**
     * Tombol Insert: tambah kategori baru.
     * - Input nama kategori
     * - Input brand
     * - Cek duplikat
     * - Simpan ke database
     */
    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        // Minta input nama kategori
        String namaKategori = javax.swing.JOptionPane.showInputDialog(
            this, 
            "Masukkan Nama Kategori:", 
            "Input Kategori", 
            javax.swing.JOptionPane.PLAIN_MESSAGE
        );
        if (namaKategori == null) return; // User klik cancel
    
        // Validasi: nama kategori gak boleh kosong
        namaKategori = namaKategori.trim();
        if (namaKategori.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nama Kategori tidak boleh kosong!");
            return;
        }

        // Minta input brand
        String brand = javax.swing.JOptionPane.showInputDialog(
            this, 
            "Masukkan Nama Brand:", 
            "Input Brand", 
            javax.swing.JOptionPane.PLAIN_MESSAGE
        );
        if (brand == null) return; // User klik cancel
    
        // Validasi: brand gak boleh kosong
        brand = brand.trim();
        if (brand.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Brand tidak boleh kosong!");
            return;
        }

        // Cek apakah kombinasi kategori + brand sudah ada di database
        if (kategoriDao.isDuplikat(namaKategori, brand)) {
            javax.swing.JOptionPane.showMessageDialog(
                this, 
                "Gagal! Kategori '" + namaKategori + "' dengan Brand '" + brand + "' sudah ada.", 
                "Data Duplikat", 
                javax.swing.JOptionPane.ERROR_MESSAGE
            );
            return; 
        }

        // Bungkus data ke objek Kategori
        Kategori k = new Kategori();
        k.setKategori(namaKategori);
        k.setBrand(brand);

        // Simpan ke database
        if (kategoriDao.insert(k)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Kategori baru berhasil ditambahkan!");
            loadKategoriToTable(); // Refresh tabel
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan data ke database.");
        }
    }//GEN-LAST:event_btninsertActionPerformed
    
     /**
     * Tombol Delete: hapus kategori yang dipilih.
     * - Cek apakah kategori masih dipakai produk
     * - Konfirmasi sebelum hapus
     */
    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
    // Cek apakah ada baris yang dipilih
        int selectedRow = tblkategori.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih kategori yang akan dihapus.");
            return;
        }

        // Ambil data dari baris yang dipilih
        int kode = (int) tblkategori.getValueAt(selectedRow, 0);
        String namaKategori = (String) tblkategori.getValueAt(selectedRow, 1);

        // Cek apakah kategori masih dipakai oleh produk
        if (kategoriDao.isDipakaiProduk(kode)) {
            javax.swing.JOptionPane.showMessageDialog(
                this, 
                "Kategori '" + namaKategori + "' masih digunakan oleh produk, tidak bisa dihapus.", 
                "Gagal Hapus", 
                javax.swing.JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        // Konfirmasi penghapusan
        int confirm = javax.swing.JOptionPane.showConfirmDialog(
            this, 
            "Yakin ingin menghapus kategori '" + namaKategori + "'?", 
            "Konfirmasi Hapus", 
            javax.swing.JOptionPane.YES_NO_OPTION
        );
        
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            if (kategoriDao.delete(kode)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Kategori berhasil dihapus.");
                loadKategoriToTable(); // Refresh tabel
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus kategori.");
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed
    
