    /**
     * Tombol Update: edit kategori yang dipilih.
     * - Input kategori baru
     * - Input brand baru
     * - Cek duplikat (kecuali sama persis dengan data lama)
     * - Simpan perubahan ke database
     */
    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
    // Cek apakah ada baris yang dipilih
        int selectedRow = tblkategori.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih kategori yang akan diupdate.");
            return;
        }

        // Ambil data lama dari baris yang dipilih
        int kode = (int) tblkategori.getValueAt(selectedRow, 0);
        String kategoriLama = (String) tblkategori.getValueAt(selectedRow, 1);
        String brandLama = (String) tblkategori.getValueAt(selectedRow, 2);

        // Minta input kategori baru (dengan default data lama)
        String kategoriBaru = javax.swing.JOptionPane.showInputDialog(
            this, 
            "Masukkan Nama Kategori baru:", 
            kategoriLama
        );
        if (kategoriBaru == null) return; // User klik cancel
        kategoriBaru = kategoriBaru.trim();
        if (kategoriBaru.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nama Kategori tidak boleh kosong.");
            return;
        }

        // Minta input brand baru (dengan default data lama)
        String brandBaru = javax.swing.JOptionPane.showInputDialog(
            this, 
            "Masukkan Brand baru:", 
            brandLama
        );
        if (brandBaru == null) return; // User klik cancel
        brandBaru = brandBaru.trim();
        if (brandBaru.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Brand tidak boleh kosong.");
            return;
        }

        // Cek duplikat (kecuali datanya sama persis dengan yang lama)
        if (kategoriDao.isDuplikat(kategoriBaru, brandBaru)) {
            if (!(kategoriLama.equals(kategoriBaru) && brandLama.equals(brandBaru))) {
                javax.swing.JOptionPane.showMessageDialog(
                    this, 
                    "Kombinasi Kategori dan Brand sudah ada!", 
                    "Duplikat", 
                    javax.swing.JOptionPane.ERROR_MESSAGE
                );
                return;
            }
        }

        // Bungkus data ke objek Kategori
        Kategori k = new Kategori(kode, kategoriBaru, brandBaru);
        
        // Simpan perubahan ke database
        if (kategoriDao.update(k)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Kategori berhasil diupdate.");
            loadKategoriToTable(); // Refresh tabel
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal mengupdate kategori.");
        }
    }//GEN-LAST:event_btnupdateActionPerformed

