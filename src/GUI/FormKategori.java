    /**
     * Load data kategori dari database ke dalam tabel.
     * Diambil semua terus dimasukin ke baris-baris tabel.
     */
    private void loadKategoriToTable() {
        // Ambil model tabelnya
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblkategori.getModel();
    
        // Kosongkan isi tabel dulu
        model.setRowCount(0); 

        // Ambil semua data kategori dari database
        java.util.List<Kategori> list = kategoriDao.getAllKategori();

        // Masukkan satu per satu ke tabel
        for (Kategori k : list) {
            Object[] row = {
                k.getKode(),
                k.getKategori(),
                k.getBrand()
            };
            model.addRow(row);
        }
    }
